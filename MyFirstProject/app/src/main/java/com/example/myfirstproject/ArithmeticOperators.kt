package com.example.myfirstproject

fun main(args: Array<String>) {
    var number1: Double = 3.4
    var number2: Double = 4.8
    var result: Double

    result = number1 + number2
    println("${number1} + ${number2} = ${result}")

    result = number1 - number2
    println("${number1} - ${number2} = ${result}")

    result = number1 * number2
    println("${number1} * ${number2} = ${result}")

    result = number1 / number2
    println("${number1} / ${number2} = ${result}")

    result = number1 % number2
    println("${number1} % ${number2} = ${result}")

}